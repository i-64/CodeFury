package com.meetingRooms.entity;

public class DataDisplayForIndex {
	
	private String name;
	
	private String seating_capacity;
	
	private String total_meetings_conducted;
	
	private String ratings;

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the seating_capacity
	 */
	public String getSeating_capacity() {
		return seating_capacity;
	}

	/**
	 * @param seating_capacity the seating_capacity to set
	 */
	public void setSeating_capacity(String seating_capacity) {
		this.seating_capacity = seating_capacity;
	}

	/**
	 * @return the total_meetings_conducted
	 */
	public String getTotal_meetings_conducted() {
		return total_meetings_conducted;
	}

	/**
	 * @param total_meetings_conducted the total_meetings_conducted to set
	 */
	public void setTotal_meetings_conducted(String total_meetings_conducted) {
		this.total_meetings_conducted = total_meetings_conducted;
	}

	/**
	 * @return the ratings
	 */
	public String getRatings() {
		return ratings;
	}

	/**
	 * @param ratings the ratings to set
	 */
	public void setRatings(String ratings) {
		this.ratings = ratings;
	}
	
	

}
